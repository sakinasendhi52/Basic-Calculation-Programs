// 9. Write a program to convert number 500 into string and display its type.

let num:number=500;               //variable in number datatype

console.log(`Before Conversion : Type of ${num} is ${typeof(num)}`);     //display type

let str:string=String(num);      //conversion into string datatype

console.log();
console.log(`After Conversion : Type of ${str} is ${typeof(str)}`);      //display type