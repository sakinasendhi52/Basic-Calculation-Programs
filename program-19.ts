// 19. Write a program to convert Celsius into Fahrenheit.

let c:number= 35;                  //Celsius in number datatype
let f:number=(9/5)*c + 32;         //Fahrenheit in number datatype and calculation

console.log("---------- Convert Celsius into Fahrenheit ----------");
console.log();
console.log(`Celsius     = ${c}°C`);          //display Celsius
console.log();
console.log(`Fahrenheit  = ${f}°F`);          //display Fahrenheit
console.log();
console.log("-----------------------------------------------------");