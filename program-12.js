// 12. Write a program to swap two numbers using third variable.
console.log("---------- Swapping Using Third Variable ----------");
let num1 = 10;
let num2 = 20;
let temp;
console.log();
console.log(`Before Swapping   :   A = ${num1}  B = ${num2}`);
console.log();
temp = num1;
num1 = num2;
num2 = temp;
console.log(`After Swapping    :   A = ${num1}  B = ${num2}`);
console.log();
console.log("--------------------------------------------------");
